def solution(n, m):
    # --- 최대공약수(GCD) 구하기 (유클리드 호제법) ---
    # 원본 값을 잃어버리면 안 되니까 임시 변수에 저장하거나 
    # 최소공배수 구할 때 원본 값 곱하기를 미리 해둠
    
    # a와 b에 각각 n, m을 복사해서 계산 시작
    a, b = max(n, m), min(n, m)
    
    while b > 0: # 나머지가 0이 될 때까지 반복
        a, b = b, a % b
    
    # 반복문이 끝나면 a가 최대공약수가 됨
    gcd_val = a
    # -------------------------------------------
    # 최소공배수 = (원래 두 수의 곱) // 최대공약수
    lcm_val = (n * m) // gcd_val
    
    return [gcd_val, lcm_val]